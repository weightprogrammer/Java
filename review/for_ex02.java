package review;

public class for_ex02 {

	public static void main(String[] args) {
		
		
		//초기화
		for(int i=1;i<=10;i++) { //변수 i값을 1로 초기화 한다.
			
		}
		for(int i=1,j=0;i<=10;i++){//int타입의 변수 i와 j를 선언 하고 초기화
			
		}
		//조건식
		for(int i=1;i<=10;i++){// i<=10 가 참인 동안 블럭{} 안의 문장들을 반복
			
		}
		//증감식
		for(int i=1;i<=10;i++){
			//1 부터 10까지 1씩 증가
		}
        for(int i=1;i>=10;i--){
        	//10부터 1까지 1씩 감소
        }
	    for(int i=1;i<=10;i+=2){
	    	//1부터 10까지 2씩 증가
	    }
		for(int i=1;i<=10;i*=3){
			//1부터 10까지 3배씩 증가
		}
	}

}
