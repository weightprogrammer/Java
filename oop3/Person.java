package kosta.oop3;

public class Person {
	private String name;
	private Role2 role;
	
	public Person() {}

	public Person(String name, Role2 role) {
		super();
		this.name = name;
		this.role = role;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Role2 getRole() {
		return role;
	}

	public void setRole(Role2 role) {
		this.role = role;
	}

	
	
	

	
	
}
